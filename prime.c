#include <math.h>
#include <stdio.h>


int main() {

    int givenNumber;

    printf("Enter a number: \n");
    scanf("%d", &givenNumber);

    if (givenNumber / givenNumber && givenNumber / 2) {
        printf("The given number is prime. \n");
    }
    else {
        printf("The given number is not prime. \n");
    }
    return 0;
}


